package com.example.coderscodes.intf;

public class Operation {
    int add(int a, int b){
        return a+b;
    }

    int add2(int a, int b){
        return 2*a + b;
    }

    int subtruct(int a, int b){
        return a-b;
    }

    int mul(int a, int b){
        return a * b;
    }
    int div(int a, int b){
        return a / b;
    }
}
